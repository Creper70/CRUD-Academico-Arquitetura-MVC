private void limparCampos(){
    txtMatricula.setText("");
    txtNome.setText("");
    txtCurso.setText("");
}
private void atualizarTabela(){
    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblAlunos.getModel();
    modelo.setNumRows(0);
    
    for (com.projetoacademico.model.Aluno a : dao.listarTodos()) {
        modelo.addRow(new Object[] {
            a.getMatricula(),
            a.getNome(),
            a.getCurso()
        });
    }
}
private void modoPadrao() {
    btnNovo.setEnabled(true);
    btnBuscar.setEnabled(true);
    btnSalvar.setEnabled(false);
    btnExcluir.setEnabled(false);
    btnAlterar.setEnabled(false);
    btnCancelar.setEnabled(false);
    
    txtMatricula.setEnabled(false);
    txtNome.setEnabled(false);
    txtCurso.setEnabled(false);
    limparCampos();
}
private void modoEdicao() {
    btnNovo.setEnabled(false);
    btnBuscar.setEnabled(false);
    btnSalvar.setEnabled(true);
    btnExcluir.setEnabled(false);
    btnAlterar.setEnabled(false);
    btnCancelar.setEnabled(true);
    
    txtMatricula.setEnabled(true);
    txtNome.setEnabled(true);
    txtCurso.setEnabled(true);
    
    txtMatricula.requestFocus();
}
private void modoBusca(boolean encontrou){
    if (encontrou) {
        btnNovo.setEnabled(false);
        btnBuscar.setEnabled(false);
        btnSalvar.setEnabled(false);
        btnExcluir.setEnabled(true);
        btnAlterar.setEnabled(true);
        btnCancelar.setEnabled(true);
    } else {
        modoPadrao();
    }
}
