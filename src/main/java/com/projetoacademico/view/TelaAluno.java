    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
try{
    String input = javax.swing.JOptionPane.showInputDialog(this, "Digite a matrícula que deseja buscar: ");
    if (input == null || input.isEmpty()) {
        return;
    }
    int matricula = Integer.parseInt(input);
    com.projetoacademico.model.Aluno aluno = dao.consultar(matricula);
    
    if (aluno != null) {
        txtMatricula.setText(String.valueOf(aluno.getMatricula()));
        txtNome.setText(aluno.getNome());
        txtCurso.setText(aluno.getCurso());
        modoBusca(true);
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Aluno não encontrado");
        modoPadrao();
    }
} catch (NumberFormatException e){
    javax.swing.JOptionPane.showMessageDialog(this, "Matrícula inválida");
} catch (Exception e){
    javax.swing.JOptionPane.showMessageDialog(this, e.getMessage());
}
// TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
try {
    if(txtMatricula.getText().isEmpty()){
throw new Exception("O campo Matrícula é obrigatório!");
    }// TODO add your handling code here:
    int matricula = Integer.parseInt(txtMatricula.getText());
    String nome = txtNome.getText();
    String curso = txtCurso.getText();
    com.projetoacademico.model.Aluno aluno = new com.projetoacademico.model.Aluno(matricula, nome, curso);
    if (dao.consultar(matricula) == null) {
        dao.incluir(aluno);
        javax.swing.JOptionPane.showMessageDialog(this, "Aluno Cadastrado com sucesso!");
    } else {
        dao.alterar(aluno);
        javax.swing.JOptionPane.showMessageDialog(this,"Dados do aluno atualizados");
    }
    atualizarTabela();
    modoPadrao();
    
} catch (NumberFormatException e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Erro: A matrícula deve ser um número inteiro", "Confirmação", javax.swing.JOptionPane.ERROR_MESSAGE);
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Erro:" + e.getMessage());
}
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
try{
    int resposta = javax.swing.JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir?", "Confirmação", javax.swing.JOptionPane.YES_NO_OPTION);

if (resposta == javax.swing.JOptionPane.YES_OPTION) {
int matricula = Integer.parseInt(txtMatricula.getText());
dao.excluir(matricula);
atualizarTabela();
javax.swing.JOptionPane.showMessageDialog(this, "Aluno Excluído");
modoPadrao();
}
} catch (Exception e) {
    javax.swing.JOptionPane.showMessageDialog(this, "Erro ao excluir:" + e.getMessage());
}// TODO add your handling code here:
    }//GEN-LAST:event_btnExcluirActionPerformed

