    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
try{ 
    if (txtCodigo.getText().isEmpty() || txtNome.getText().isEmpty() || txtCargaHoraria.getText().isEmpty()) {
throw new Exception("Preencha todos os campos!");
    }
String codigo = txtCodigo.getText();
String nome = txtNome.getText();

int carga = Integer.parseInt(txtCargaHoraria.getText());

com.projetoacademico.model.Disciplina d = new com.projetoacademico.model.Disciplina(codigo, nome, carga);
if (dao.consultar(codigo) == null) {
dao.incluir(d);
javax.swing.JOptionPane.showMessageDialog(this,"Salvo com Sucesso");
} else {
dao.alterar(d);
javax.swing.JOptionPane.showMessageDialog(this, "Alterado com sucesso");
}

atualizarTabela();
modoPadrao();
txtCodigo.setText(""); txtNome.setText(""); txtCargaHoraria.setText("");
} catch (NumberFormatException e) {
javax.swing.JOptionPane.showMessageDialog(this, "A Carga Horária deve ser um número");
} catch (Exception e) {
javax.swing.JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
}// TODO add your handling code here:
    }//GEN-LAST:event_btnSalvarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
try{
    String codigoBusca = javax.swing.JOptionPane.showInputDialog(this, "Digite o código da disciplina: ");
if (codigoBusca == null || codigoBusca.isEmpty()) {
    return;
}
com.projetoacademico.model.Disciplina d = dao.consultar(codigoBusca);

if (d != null) {
txtNome.setText(d.getNome());
txtCargaHoraria.setText(String.valueOf(d.getCargaHoraria()));
txtCodigo.setText(d.getCodigo());
modoBusca();
} else {
javax.swing.JOptionPane.showMessageDialog(this, "Disciplina não encontrada");
modoPadrao();
}
} catch (Exception e) {
javax.swing.JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
}// TODO add your handling code here:
    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
try {
    String codigo = txtCodigo.getText();
int confirma = javax.swing.JOptionPane.showConfirmDialog(this, "Excluir?", "Tem certeza?", javax.swing.JOptionPane.YES_NO_OPTION);

if (confirma == javax.swing.JOptionPane.YES_OPTION) {
dao.excluir(codigo);
atualizarTabela();
javax.swing.JOptionPane.showMessageDialog(this, "Excluído");
modoPadrao();
}
} catch (Exception e) {
javax.swing.JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
}// TODO add your handling code here:
    }//GEN-LAST:event_btnExcluirActionPerformed

