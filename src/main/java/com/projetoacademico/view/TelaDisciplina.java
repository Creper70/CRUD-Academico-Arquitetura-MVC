private void atualizarTabela() {
    javax.swing.table.DefaultTableModel modelo = (javax.swing.table.DefaultTableModel) tblDisciplinas.getModel();
    modelo.setNumRows(0);
    
    for (com.projetoacademico.model.Disciplina d : dao.listarTodos()) {
        modelo.addRow(new Object[] {
            d.getCodigo(),
            d.getNome(),
            d.getCargaHoraria()
        });
    }
}
private void modoPadrao() {
    btnNovo.setEnabled(true);
    btnBuscar.setEnabled(true);
    btnSalvar.setEnabled(false);
    btnAlterar.setEnabled(false);
    btnExcluir.setEnabled(false);
    btnCancelar.setEnabled(false);
    
    txtCodigo.setEnabled(false);
    txtNome.setEnabled(false);
    txtCargaHoraria.setEnabled(false);
    
    txtCodigo.setText("");
    txtNome.setText("");
    txtCargaHoraria.setText("");
}
private void modoEdicao() {
    btnNovo.setEnabled(false);
    btnBuscar.setEnabled(false);
    btnSalvar.setEnabled(true);
    btnCancelar.setEnabled(true);
    btnAlterar.setEnabled(false);
    btnExcluir.setEnabled(false);
    
    txtCodigo.setEnabled(true);
    txtNome.setEnabled(true);
    txtCargaHoraria.setEnabled(true);
    
    txtCodigo.requestFocus();
}
private void modoBusca(){
    btnNovo.setEnabled(false);
    btnBuscar.setEnabled(false);
    btnSalvar.setEnabled(false);
    btnAlterar.setEnabled(true);
    btnExcluir.setEnabled(true);
    btnCancelar.setEnabled(true);
}
